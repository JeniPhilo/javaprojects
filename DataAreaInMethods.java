package classOnes;



public class DataAreaInMethods {


	public double area(double b, double h) {

		double area = (b * h) * .5;
		return area;

	}

	public double area(double a) {
		double area = a * a;
		return area;
	}
	public float area (float r) {
		float area= (float) (3.14*(r*r));
		return area;
		
	}

}
